public class ex15 {

/*Crie um programa Java que demonstre o valor padrão de variáveis primitivas (int,
double, char, boolean) quando não inicializadas explicitamente.
 */

    static int nuInt;
    static double nuDouble;
    static char palavra;
    static boolean boleano; 

    public static void main(String[] args) {
        
        System.out.println(nuInt);
        System.out.println(nuDouble);
        System.out.println(palavra);
        System.out.println(boleano);
        
    }
    
}
